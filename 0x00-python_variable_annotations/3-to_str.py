#!/usr/bin/env python3
"""
This is a module that provides a function for converting a float to a string.
"""


def to_str(n: float) -> str:
    """
    Converts a floating-point number to its string representation.

    Args:
        n (float): The floating-point number.

    Returns:
        str: The string representation of `n`.
    """
    return str(n)
